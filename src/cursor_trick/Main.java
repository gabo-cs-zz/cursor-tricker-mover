    public void mouseMover(){

        try {
            Random random = new Random();
            PointerInfo a = MouseInfo.getPointerInfo();
            Point b = a.getLocation();
            int x = (int) b.getX();
            int y = (int) b.getY();
            Robot r = new Robot();
            int rnd = random.nextInt(5) - 2;
            r.mouseMove(x + rnd, y);
        } catch (AWTException e) {
            System.out.println(e.getMessage());
        }
    }

