    private void cmdOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdOkActionPerformed
        // TODO add your handling code here:
        //if
        try {
            int tf = Integer.parseInt(txtSecs.getText().trim());
            if (tf > 180 || tf == 0) {
                JOptionPane.showMessageDialog(this, "Cannot be higher than 180 nor equal to 0.","ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
               time = tf*1000;
               timer.setDelay(time);
               cmdStart.setEnabled(true);
               txtSecs.setEditable(false);
               cmdOk.setEnabled(false);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Enter a valid number.","ERROR", JOptionPane.ERROR_MESSAGE);
            txtSecs.requestFocusInWindow();
        }

    }//GEN-LAST:event_cmdOkActionPerformed

